package modelo;

/**
 *
 * @author Jhonathan
 */
public class Sucursales {
    
    int id;
    String nombre,direccion,fecha,tipo;

    public Sucursales() {
    }

    public Sucursales(int id, String nombre, String direccion, String fecha, String tipo) {
        this.id = id;
        this.nombre = nombre;
        this.direccion = direccion;
        this.fecha = fecha;
        this.tipo = tipo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
